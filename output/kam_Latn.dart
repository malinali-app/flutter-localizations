        : (timeOfDay.hour % 12 == 0 ? 12 : timeOfDay.hour % 12);
    final minute = timeOfDay.minute.toString().padLeft(2, '0');
    final period = alwaysUse24HourFormat
        ? ''
        : (timeOfDay.period == DayPeriod.am ? ' AM' : ' PM');
    return '$hour:$minute$period';
